package com.fssa.librarymanagement.constants;

/**
 * This class holds a collection of constant success messages for various operations in the library management system.
 * These messages are used to provide feedback to users when actions are executed successfully.
 */
public class SuccessMessageConstants {

	/**
	 * Message indicating that a book has been successfully borrowed.
	 */
	public static final String BOOK_BORROWED_SUCCESSFULLY = "Book borrowed successfully.";

	/**
	 * Message indicating that a return date has been successfully added.
	 */
	public static final String ADDED_RETURN_DATE_SUCCESSFULLY = "Added return date successfully.";

	/**
	 * Message indicating that a book has been successfully added to the library.
	 */
	public static final String BOOK_ADDED_SUCCESSFULLY = "Book added successfully.";

	/**
	 * Message indicating that user registration was successful.
	 */
	public static final String REGISTRATION_SUCCESSFUL = "Registration successful.";

	/**
	 * Private constructor to prevent instantiation of this utility class.
	 */
	private SuccessMessageConstants() {
		// Private constructor to prevent instantiation
	}
}
